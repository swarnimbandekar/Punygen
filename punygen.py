def puny_variants(char):
    table = Table(title=f"Unicode Lookalikes & Punycode for '{char}'", style="bold cyan")

    table.add_column("Variant", style="bold yellow", justify="center")
    table.add_column("Punycode", style="green")

    found = False
    for variant in lookalikes.get(char.lower(), []):
        found = True
        try:
            punycode = idna.encode(f"{variant}.com").decode().replace(".com", "")
            table.add_row(variant, f"xn--{punycode}")
        except idna.IDNAError:
            table.add_row(variant, "[red]Invalid IDNA")

    if found:
        console.print(table)
    else:
        console.print(f"[bold red]No homoglyphs found for '{char}'")

